        "<i>–í–∞–∂–Ω–æ: –Ø ‚Äî –Ω–µ–π—Ä–æ—Å–µ—Ç—å, –∞ –Ω–µ —Å–≤—è—â–µ–Ω–Ω–∏–∫. –ú–æ–∏ –æ—Ç–≤–µ—Ç—ã –æ—Å–Ω–æ–≤–∞–Ω—ã –Ω–∞ –ø—Ä–∞–≤–æ—Å–ª–∞–≤–Ω—ã—Ö —É—á–µ–Ω–∏—è—Ö –∏ —Ç–µ–∫—Å—Ç–∞—Ö, "
